package constructor;

public class ConstructorDocs {
	
	/*
	 * ======================= Constructor =======================
	 *  # use to create object in java.
	 *  
	 *  # special type of method which has same name as class name
	 *    is known as constructor.
	 *   
	 *  # if there is no any user defined constructor in class
	 *     then java provides default constructor.
	 *     
	 *   # Types :
	 *   
	 *        a> Default constructor :
	 *          # use to create object with its default values.
	 *          
	 *          syntax :
	 *          
	 *           class_name(){
	 *           
	 *               //set default values to object
	 *           }
	 *        
	 *        b> Parameterized constructor:
	 *        	# use to create object with its dynamic values.
	 *        
	 *        syntax :
	 *        
	 *        	class_name(arg1, arg2,....,argn){
	 *        
	 *               //set dynamic values to object
	 *           }
	 *     
	 */

}
